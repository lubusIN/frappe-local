import path from 'node:path';
import fs from 'node:fs';
import { DATABASE_CREDENTIALS } from '../constants';

/** Hidden folder inside the bench directory that stores infrastructure files. */
export const LOCAL_BENCH_DIR = '.local-bench';

/**
 * Returns the path to the docker-compose file for a given bench.
 */
export const getBenchComposePath = (benchPath: string): string =>
  path.join(benchPath, LOCAL_BENCH_DIR, 'docker-compose.yml');

/**
 * Maps a Frappe version string to the correct Docker image tag.
 * e.g. 'version-16' → 'frappe/frappe-worker:version-16'
 */
export const getImageTag = (frappeVersion: string): string => {
  const tag = frappeVersion === 'develop' ? 'develop' : frappeVersion;
  return `docker.io/frappe/erpnext:${tag}`;
};

export interface BenchComposeOptions {
  frappeVersion: string;
  httpPort: number;
}

/**
 * Generates the docker-compose YAML content for a bench.
 *
 * The compose file lives at .local-bench/docker-compose.yml, so volume
 * paths use '../sites' and '../apps' to resolve back to the bench root.
 */
export const generateBenchCompose = (options: BenchComposeOptions): string => {
  const { frappeVersion, httpPort } = options;
  const image = getImageTag(frappeVersion);
  // Socket.io port derived from httpPort to avoid collisions
  const socketPort = httpPort + 1000;
  const dbPassword = DATABASE_CREDENTIALS.DB_PASSWORD;

  return `# Generated by Local Bench – do not edit manually.
# Re-generated on every bench start.
services:
  configurator:
    image: ${image}
    user: root
    entrypoint: ["bash", "-c"]
    command:
      - >
        cp -rn /home/frappe/frappe-bench/assets/. /home/frappe/frappe-bench/sites/assets/;
        cp -rn /home/frappe/frappe-bench/apps/. /var/local-bench/apps/;
        chown -R 1000:1000 /home/frappe/frappe-bench/sites/assets/ /var/local-bench/apps/;
    volumes:
      - ../sites:/home/frappe/frappe-bench/sites
      - ../apps:/var/local-bench/apps

  backend:
    image: ${image}
    volumes:
      - ../sites:/home/frappe/frappe-bench/sites
      - ../apps:/home/frappe/frappe-bench/apps
    environment:
      - DB_HOST=db
      - DB_ROOT_PASSWORD=${dbPassword}
      - REDIS_CACHE=redis://redis:6379
    depends_on:
      configurator:
        condition: service_completed_successfully
      db:
        condition: service_healthy
    healthcheck:
      test: ["CMD", "ls", "sites/apps.txt"]
      interval: 5s
      timeout: 5s
      retries: 60

  frontend:
    image: ${image}
    command: nginx-entrypoint.sh
    ports:
      - "127.0.0.1:${httpPort}:8080"
    environment:
      - BACKEND=backend:8000
      - SOCKETIO=websocket:9000
    volumes:
      - ../sites:/home/frappe/frappe-bench/sites
    depends_on:
      backend:
        condition: service_healthy

  websocket:
    image: ${image}
    command: node /home/frappe/frappe-bench/apps/frappe/socketio.js
    ports:
      - "127.0.0.1:${socketPort}:9000"
    volumes:
      - ../sites:/home/frappe/frappe-bench/sites
      - ../apps:/home/frappe/frappe-bench/apps
    depends_on:
      backend:
        condition: service_healthy

  db:
    image: docker.io/mariadb:10.6
    environment:
      - MARIADB_ROOT_PASSWORD=${dbPassword}
    healthcheck:
      test: ["CMD-SHELL", "mysqladmin ping -h localhost -p\$\${MARIADB_ROOT_PASSWORD} || exit 1"]
      interval: 5s
      timeout: 5s
      retries: 10

  redis:
    image: docker.io/redis:7-alpine
`;
};

/**
 * Ensures the .local-bench directory exists and writes (or overwrites)
 * the docker-compose.yml for the given bench configuration.
 *
 * @returns The absolute path to the written compose file.
 */
export const ensureBenchComposeWritten = (
  benchPath: string,
  frappeVersion: string,
  httpPort: number
): string => {
  const localBenchDir = path.join(benchPath, LOCAL_BENCH_DIR);
  if (!fs.existsSync(localBenchDir)) {
    fs.mkdirSync(localBenchDir, { recursive: true });
  }

  const composePath = getBenchComposePath(benchPath);
  const content = generateBenchCompose({ frappeVersion, httpPort });
  fs.writeFileSync(composePath, content, 'utf8');
  return composePath;
};
