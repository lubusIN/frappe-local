import path from 'node:path';
import fs from 'node:fs';
import { DATABASE_CREDENTIALS } from '../../constants';

/** Hidden folder inside the bench directory that stores infrastructure files. */
export const FRAPPE_LOCAL_DIR = '.frappe-local';

/**
 * Returns the path to the docker-compose file for a given bench.
 */
export const getBenchComposePath = (benchPath: string): string =>
  path.join(benchPath, FRAPPE_LOCAL_DIR, 'docker-compose.yml');

/**
 * Returns the official frappe bench development image.
 */
export const getImageTag = (): string => {
  return `docker.io/frappe/bench:latest`;
};

export interface BenchComposeOptions {
  frappeVersion: string;
  httpPort: number;
}

/**
 * Generates the docker-compose YAML content for a bench.
 */
export const generateBenchCompose = (options: BenchComposeOptions): string => {
  const { httpPort } = options;
  const image = getImageTag();
  // Socket.io port derived from httpPort to avoid collisions
  const socketPort = httpPort + 1000;
  const dbPassword = DATABASE_CREDENTIALS.DB_PASSWORD;

  return `# Generated by Frappe Local – do not edit manually.
# Re-generated on every bench start.
services:
  frappe:
    image: ${image}
    command: sleep infinity
    user: frappe
    environment:
      SHELL: /bin/bash
    volumes:
      - ../:/workspace:cached
    ports:
      - "127.0.0.1:${httpPort}:8000"
      - "127.0.0.1:${socketPort}:9000"
    depends_on:
      mariadb:
        condition: service_healthy
      redis:
        condition: service_started
    working_dir: /workspace

  mariadb:
    image: docker.io/mariadb:10.6
    command:
      - --character-set-server=utf8mb4
      - --collation-server=utf8mb4_unicode_ci
      - --skip-character-set-client-handshake
    environment:
      MYSQL_ROOT_PASSWORD: "${dbPassword}"
      MARIADB_AUTO_UPGRADE: "1"
    volumes:
      - mariadb-data:/var/lib/mysql
    healthcheck:
      test: ["CMD-SHELL", "mysqladmin ping -h localhost -u root -p$\${MYSQL_ROOT_PASSWORD} || exit 1"]
      interval: 5s
      timeout: 5s
      retries: 15

  redis:
    image: docker.io/redis:alpine

volumes:
  mariadb-data:
`;
};

/**
 * Ensures the .frappe-local directory exists and writes (or overwrites)
 * the docker-compose.yml for the given bench configuration.
 *
 * @returns The absolute path to the written compose file.
 */
export const ensureBenchComposeWritten = (
  benchPath: string,
  frappeVersion: string,
  httpPort: number
): string => {
  const frappeLocalDir = path.join(benchPath, FRAPPE_LOCAL_DIR);
  if (!fs.existsSync(frappeLocalDir)) {
    fs.mkdirSync(frappeLocalDir, { recursive: true });
  }

  const composePath = getBenchComposePath(benchPath);
  const content = generateBenchCompose({ frappeVersion, httpPort });
  fs.writeFileSync(composePath, content, 'utf8');
  return composePath;
};
